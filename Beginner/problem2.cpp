// Demonstration of compound assignments
// Operators for Fundamental Types

#include <iostream>
using namespace std;

int main()
{
    float num1, num2;

    cout << "For Addition:";
    cin >> num1 >> num2;
    cout << "The sum is: " << num1 + num2;

    cout << "\nFor Subtraction:";
    cin >> num1 >> num2;
    cout << "The sub is: " << num1 - num2;

    cout << "\nFor multiplication:";
    cin >> num1 >> num2;
    cout << "The product is: " << num1 * num2;

    cout << "\nFor division:";
    cin >> num1 >> num2;
    cout << "The divident is: " << num1 / num2;
}
